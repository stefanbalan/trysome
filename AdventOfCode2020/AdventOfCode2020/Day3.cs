        public int Compute1()
        {
            var count = 0;
            int ix = 0, iy = 0;

            LoadMap();

            while (iy < height - 1)
            {
                ix += 3; iy += 1;
                var ax = (ix % width);
                if (map[iy][ax]) count += 1;
            }

            return count;
        }

        public string Compute2()
        {
            var rs = "";
            long rn = 1;

            var n = Compute(1, 1);
            rs += " " + n;
            rn *= n;

            n = Compute(3, 1);
            rs += " " + n;
            rn *= n;

            n = Compute(5, 1);
            rs += " " + n;
            rn *= n;

            n = Compute(7, 1);
            rs += " " + n;
            rn *= n;

            n = Compute(1, 2);
            rs += " " + n;
            rn *= n;

            rs += " = " + rn;
            return rs;
        }

        private int Compute(int xStep, int yStep)
        {
            var count = 0;
            int ix = 0, iy = 0;

            LoadMap();

            while (iy < height - 1)
            {
                ix += xStep; iy += yStep;
                var ax = (ix % width);
                if (map[iy][ax]) count += 1;
            }

            return count;
        }

        private void LoadMap()
        {
            var stream = new MemoryStream(Encoding.ASCII.GetBytes(input));
            var reader = new StreamReader(stream);

            map = new List<List<bool>>();
            var str = reader.ReadLine();
            width = str.Length;

            while (!string.IsNullOrEmpty(str))
            {
                var line = new List<bool>(str.Length);
                line.AddRange(str.Select(c => c.Equals('#')));
                map.Add(line);
                str = reader.ReadLine();
            }

            height = map.Count;
        }

